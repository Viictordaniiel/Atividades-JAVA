package Atv;

public class Cliente {
	
	    private String nome;
	    private String numeroConta;

	    public Cliente(String nome, String numeroConta) {
	        this.nome = nome;
	        this.numeroConta = numeroConta;
	    }

	    public String getNome() {
	        return nome;
	    }

	    public String getNumeroConta() {
	        return numeroConta;
	    }
	}


